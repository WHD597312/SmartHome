package com.xinrui.smart.pojo;

public class CommonSet {
    String s1;
    String s2;
    int img;

    public CommonSet(String s1, String s2, int img) {
        this.s1 = s1;
        this.s2 = s2;
        this.img = img;
    }

    public String getS1() {
        return s1;
    }

    public void setS1(String s1) {
        this.s1 = s1;
    }

    public String getS2() {
        return s2;
    }

    public void setS2(String s2) {
        this.s2 = s2;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
